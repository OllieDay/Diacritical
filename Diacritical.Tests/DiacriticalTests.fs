    [<Theory>]
    [<InlineData ("yi2huir4", "yíhuìr")>]
    [<InlineData ("yi2huiri4", "yíhuirì")>]
    let ``should convert "er"`` (text, pinyin) =
        Diacritical.convert text
            |> should equal pinyin

    [<Fact>]
    let ``should skip text that cannot be converted`` () =
        Diacritical.convert "hi7 there5 this is ni3hao3ma ok then"
            |> should equal "hi7 there this is nǐhǎoma ok then"

    [<Fact>]
    let ``should not convert pinyin if text is invalid`` () =
        Diacritical.convert "ki3"
            |> should equal "ki3"

